package poly.dto;

public class MelonSongDTO {

	private String song; // 노래제목
	private String rank; // 현재 랭킹

	public String getSong() {
		return song;
	}

	public void setSong(String song) {
		this.song = song;
	}

	public String getRank() {
		return rank;
	}

	public void setRank(String rank) {
		this.rank = rank;
	}

}

